#整数（Integers）
x = 7      # 正整数
y = -7     # 负整数
z = 0      # 零

#浮点数（Floating Point Numbers）
a = 3.14159  # 浮点数
b = -2.8    # 负浮点数
c = 0.0     # 浮点数形式的零

#字符串（Strings）
s1 = 'Hello, world!'  # 使用单引号
s2 = "Python 3.9"     # 使用双引号
s3 = """This is a
multi-line string."""  # 使用三引号

#布尔值（Booleans）
is_valid = True   # 真
is_active = False # 假

print (x, y, z, a, b, c, s1, s2, s3, is_valid, is_active)