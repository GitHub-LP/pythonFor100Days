# 算数运算符
a = 10
b = 6

# 加法
add_result = a + b

# 减法
subtract_result = a - b

# 乘法
multiply_result = a * b

# 除法
divide_result = a / b

# 整除
floor_div_result = a // b

# 取模
mod_result = a % b

# 幂
power_result = a ** b

# 比较运行符
# 大于
greater_than_result = a > b

# 小于
less_than_result = a < b

# 大于等于
greater_than_equal_result = a >= b

# 小于等于
less_than_equal_result = a <= b

# 等于
equal_result = a == b

# 不等于
not_equal_result = a != b

# 逻辑运算符
# 逻辑与
1 and 0  # 0
True and False  # False


# 逻辑或
0 or 1  # 1
True or False # True

# 逻辑非
1 not in [0, 1]  # False
not True  # False